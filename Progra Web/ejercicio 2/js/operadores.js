var a = 10, b = 5;

//Suma
var resultado = a + b;
console.log(resultado);

//Resta
var resultado = a - b;
console.log(resultado);

//Multiplicacion
var resultado = a * b;
console.log(resultado);

//Division
var resultado = a / b;
console.log(resultado);

//Modulo sobrante
var resultado = a % b;
console.log(resultado);

//Incremento en ventana de alerta
var numero = 5;
++numero,
alert(numero);
console.log(numero);

//Decremento en ventana de alerta
var numero = 5;
--numero;
